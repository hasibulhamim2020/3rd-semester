/*Write a program to declare two integer type variables, two floating type
variables, two double type variables and two characters type variables.
Then assign values to those variables and print them.*/
#include<stdio.h>
int main()
     {
         int a=1,b=10;
         float x=0.036,y=2.005;
         double c=123456,d=987654;
         char ch='x',ch1='z';

              printf("the integer number =%d ,%d\n",a,b);
              printf("the float number = %f ,%f\n",x,y);
              printf("the double type number = %lf ,%lf\n",c,d);
              printf("the character = %c ,%c\n",ch,ch1);

         return 0;
     }
